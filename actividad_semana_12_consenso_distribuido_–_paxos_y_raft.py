if __name__ == "__main__":
    cluster = {}
    # Inicializar 3 nodos
    for i in range(1, 4):
        cluster[i] = Node(node_id=i, all_nodes=cluster)

    time.sleep(3) # Esperar a que ocurra la primera elección de líder

    # Encontrar quién es el líder actual
    leader_node = None
    for n in cluster.values():
        if n.state == "Leader":
            leader_node = n
            break

    # 1. Proponer un valor inicial exitoso
    if leader_node:
        leader_node.propose_value("A=1")

    time.sleep(2)
    print("\n--- SIMULANDO FALLO: Tumbando al líder actual ---")
    old_leader_id = leader_node.node_id
    cluster[old_leader_id].is_alive = False
    cluster[old_leader_id].state = "Follower"
    print(f"-> El Nodo {old_leader_id} (Líder) ha dejado de responder por completo.\n")

    # Esperar a que los followers noten la ausencia y elijan un nuevo líder
    time.sleep(4)

    # Buscar el nuevo líder
    new_leader = None
    for n in cluster.values():
        if n.state == "Leader":
            new_leader = n
            break

    # 2. Proponer un nuevo valor con el nuevo líder para validar resiliencia
    if new_leader:
        print(f"\n--- ENVIANDO PROPUESTA AL NUEVO LÍDER (Nodo {new_leader.node_id}) ---")
        new_leader.propose_value("A=2")
    else:
        print("No se pudo consolidar un nuevo líder a tiempo.")

    time.sleep(2)
